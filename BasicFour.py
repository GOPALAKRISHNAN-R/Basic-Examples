# tuple 
t1=(1,2,33,44,66)
print(t1)						# prints single line

# print second value 
print(t1[1])					# index value 1

# print last value 
print(t1[-1])

# print except 1st value
print(t1[1:])

# print except last value
print(t1[:-1])

# print intermediate values 
print(t1[1:3])

# print last second value 
print(t1[-2])

# print whole values
print(t1[:])